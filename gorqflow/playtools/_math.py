def add_one(x: int) -> int:
    """Add one to an integer

    Args:
        x (int): The base value

    Returns:
        int: x + 1
    """
    return x + 1


def sub_one(x: int) -> int:
    """Subtract one from an integer

    Args:
        x (int): The base value

    Returns:
        int: x - 1
    """
    return x - 1
